public class switchCase4 {
    public static void main(String[] args){
        int num=900;
        switch(num){

            case 200: System.out.println("case 1st ");
            break;
            case 300: System.out.println("case 2nd ");
            break;
            case 100: System.out.print("case 3rd ");
            break;
            default: System.out.print("NO case ");
            break;
        }
    }
}
